package brick.particulas.smodel;

public class LineOrientMeassure extends Meassure {

	private boolean line;
	private float orientation;
	private boolean tocoizq;
	private boolean tocoder;

	public LineOrientMeassure(boolean line, 
			float orientation, boolean tocoizq, boolean tocoder){
		this.line = line;
		this.orientation = orientation;
		this.tocoizq = tocoizq;
		this.tocoder = tocoder;
	}

	public boolean isLine() {
		return line;
	}

	public void setLine(boolean line) {
		this.line = line;
	}

	public float getOrientation() {
		return orientation;
	}

	public void setOrientation(float orientation) {
		this.orientation = orientation;
	}

	public boolean isTocoizq() {
		return tocoizq;
	}

	public void setTocoizq(boolean tocoizq) {
		this.tocoizq = tocoizq;
	}

	public boolean isTocoder() {
		return tocoder;
	}

	public void setTocoder(boolean tocoder) {
		this.tocoder = tocoder;
	}

	@Override
	public String toString() {
		return line+","+orientation+","+tocoizq+","+tocoder;
	}
	
	
}
